package JavaConditionalStatement;

public class fivetables {
// 5 * 1 = 5
// 5 * 2 = 10
	
	
// 5 * 10 = 50
	public static void main(String[] args) {
		
		int number = 5;
		int multi ;
		for(int i=1;i<=10;i++) {
			multi = i*number;
			System.out.println(number + "* " + i + " " + "= " + multi);
		}
		
		
	}

}
